turno = input("Informe o turno que você estuda: M para matutino, V para vespertino ou N para noturno: ")
if (turno == "M"):
  print("Bom dia!")
elif (turno == "V"):
  print("Boa tarde!")
elif (turno == "N"):
  print("Boa noite!")
else:
  print("Informe uma das 3 opções informadas: M para matutino, V para vespertino ou N para noturno")
